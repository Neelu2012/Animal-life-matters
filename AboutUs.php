                                 <h3>While Diwali celebrations are fun for people, it is one of the most stressful times
                for animals who are terrified by fireworks. This year, let’s celebrate the festival in a considerate
                manner.

                Heres what we can do :
                <h1> Dont burst crackers near animals.</h1>
                Refrain from bursting crackers near an animal. Loud noises can cause panic and fear. The cracker residue
                can be consumed by them and lead to serious health issues. Pollution from burning firecrackers is also
                harmful to their health.

                <h1>Dont toy around with animals.</h1>
                Don’t throw crackers at them or tie it to their tails or mouths. The crackers may burn their bodies and
                leave them traumatised and suffering for life or even lead to their death.

                <h1>Dont tease animals.</h1>
                Some animals may act aggressively due to anxiety, while some may hide in a silent isolated corner away
                from the lights and noise. Some of them may urinate, defecate, drool, or pant excessively. Don’t shout
                at them, tease them or force them out of their hiding state. Allow them to find comfort in their own
                way.

                <h1>Dont leave loose wires hanging.</h1>
                Crackers are not the only danger during Diwali. Loosely tethered electric wires can lure animals to
                touch them. They can accidentally get entangled in these wires and are at the risk of getting
                electrocuted. Make sure the wires are safely sealed and away from the reach of the animals.

                <h1>Dont give spicy food or sweets to animals.</h1>
                With Diwali, you’ll no doubt have a whole lot of sweets and savouries at home, but avoid feeding them to
                the animals. These foods can upset their stomachs. It’s best to stick to their usual diets.

                If you come across any injured animals or birds, immediately contact the nearest help centre.
